

function incrementRed(){
    var element = document.getElementById('red');
    var value = element.innerHTML;

    ++value;

    console.log(value);
    document.getElementById('red').innerHTML = value;
    }
function decrementRed(){
    var element = document.getElementById('red');
    var value = element.innerHTML;

    if(value >= 1)
    --value;
    

    console.log(value);
    document.getElementById('red').innerHTML = value;
}

